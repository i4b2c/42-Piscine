#include <unistd.h>

void	ft_is_negative(int n)
{
	char	b;
	char	c;

	b = 'N';
	c = 'P';
	if (n < 0)
	{
		write (1, &b, 1);
	}
	if (n >= 0)
	{
		write (1, &c, 1);
	}
}
