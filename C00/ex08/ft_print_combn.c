#include <unistd.h>

void	write_func(char a)
{
	write(1, &a, 1);
}

void	ft_print_combn(int n)
{
	int	a;

	a = '0' ;
	if (n == 1)
	{
		while (a < 9)
		{
			write_func(a + 1);
		}
	}
}
